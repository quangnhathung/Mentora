/** xfnv1a -> 32-bit unsigned hash (dùng để tạo seed từ chuỗi) */
function xfnv1aHash(str: string): number {
  let h = 2166136261 >>> 0;
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    h = Math.imul(h, 16777619) >>> 0;
  }
  return h >>> 0;
}

/** mulberry32 PRNG (tạo từ seed 32-bit) */
function mulberry32(seed: number) {
  let t = seed >>> 0;
  return function () {
    t += 0x6d2b79f5;
    t = Math.imul(t ^ (t >>> 15), t | 1) >>> 0;
    t ^= (t + Math.imul(t ^ (t >>> 7), t | 61)) >>> 0;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

/**
 * Trả về `count` phần tử cố định cho 1 ngày (timezone Asia/Ho_Chi_Minh).
 * - arr: nguồn từ điển
 * - count: số phần tử cần lấy (mặc định 10)
 * - dateStr (tùy): truyền "YYYY-MM-DD" để kiểm thử; nếu không truyền sẽ lấy ngày hiện tại theo Asia/Ho_Chi_Minh
 * - salt (tùy): thêm salt để thay đổi tập từ cho cùng 1 ngày (vd: userId)
 */
export function getDailyVocab<T>(arr: T[], count = 10, dateStr?: string, salt = ''): T[] {
  if (!arr || arr.length === 0) return [];

  // Lấy ngày hiện tại theo timezone Asia/Ho_Chi_Minh nếu không truyền dateStr
  const day = dateStr || new Date().toLocaleDateString('en-CA', { timeZone: 'Asia/Ho_Chi_Minh' }); // "YYYY-MM-DD"

  const seed = xfnv1aHash(day + '|' + salt);
  const rng = mulberry32(seed);

  const n = Math.min(count, arr.length);
  const copy = arr.slice(); // ko đổi mảng gốc

  // partial Fisher–Yates dùng rng có seed
  for (let i = 0; i < n; i++) {
    const j = i + Math.floor(rng() * (copy.length - i));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }

  return copy.slice(0, n);
}
